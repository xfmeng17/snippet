#!/bin/bash
# =============================================================================
# 生成 ~/.zshrc
# =============================================================================
set -e

TARGET="$HOME/.zshrc"

# 备份已有文件
if [ -f "$TARGET" ]; then
    BACKUP="${TARGET}.bak.$(date +%s)"
    echo ">>> 备份已有 $TARGET -> $BACKUP"
    cp "$TARGET" "$BACKUP"
fi

echo ">>> 生成 $TARGET"
cat > "$TARGET" << 'ZSHRC_EOF'
# =============================================================================
#                         ~/.zshrc — Zsh 特有配置
#              通用配置 (环境变量、别名、PATH) 在 ~/.profile 中
# =============================================================================


# #############################################################################
#                          Oh My Zsh
# #############################################################################

export ZSH="$HOME/.oh-my-zsh"
ZSH_THEME="fletcherm"
ZSH_DISABLE_COMPFIX="true"    # 跳过不安全目录的验证（需在 source oh-my-zsh.sh 之前）
plugins=(git zsh-autosuggestions zsh-syntax-highlighting)

source $ZSH/oh-my-zsh.sh


# #############################################################################
#                       加载通用配置 (~/.profile)
# #############################################################################

if [ -f ~/.profile ]; then
    source ~/.profile
fi


# #############################################################################
#                       Zsh 提示符
# #############################################################################

export PS1='%F{green}%n%f:%F{blue}%~%f$ '
ZSHRC_EOF

echo ">>> ~/.zshrc 生成完成"
